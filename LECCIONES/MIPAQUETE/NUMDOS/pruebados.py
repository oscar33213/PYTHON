"""ESTA MODULO PERMITE OPERACIONES SIMPLES"""
def sumar(op1, op2):
    print("El resultado es: ", op1+op2)
    
def restar(op1, op2):
    print("El resultado es: ", op1-op2)

def multiplicar(op1, op2):
    print("El resultado es: ", op1*op2)
    
def potencia (base, exponente):
    print("El resultado es: ", base**exponente)


    